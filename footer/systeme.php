I.	La veille
La veille est une discipline apparue récemment, elle découle d'une prise de conscience de la nécessité de mettre en place une nouvelle forme de gestion de l'information tournée vers des notions de qualité et non plus de quantité. En ce sens, elle est un outil qui permet aux entreprises d'y voir plus clair dans la masse importante d'informations aujourd'hui disponible grâce à Internet et à l'évolution rapide des TIC.
L'activité de veille permet à l'entreprise de recueillir les données informationnelles significatives dans un contexte caractérisé par l'afflux d'informations de diverses formes. Cela suscite la mise en place d'un système de surveillance de l'environnement basé sur une démarche rationnelle et une méthodologie claire.
La veille est donc l'observation et l'analyse de l'environnement scientifique, technique, technologique et économique de l'entreprise pour détecter les menaces et saisir les opportunités de développement.
</p>III.	Veille et surveillance 
<p>Il faut distinguer entre « veille » et « surveillance » :
•	La veille : est une pratique qui englobe les actions de collecte, d'analyse et de diffusion des informations en vue de rendre plus intelligible l'environnement de l'entreprise. En cherchant à anticiper les évolutions du marché par une mise en valeur des informations et des connaissances, la veille a pour objectif d'accroître l'adaptabilité de l'entreprise à son marché ;
•	La surveillance : est une collecte régulière d'informations pertinentes pour une entreprise ou un organisme et sa communication à un nombre restreint d'intéressés.
IV.	Les acteurs de la veille
	Le processus de veille n'est pas une tâche isolée d'une ou deux personnes. Réellement, il est un processus transversal, fondé sur des réseaux, faisant intervenir plusieurs personnes situées à plusieurs niveaux et de manière organisée. Le processus de veille met en jeu trois(3) types d'acteurs qui sont les suivants : l’animateur, les veilleurs et les décideurs.  Ces acteurssont des activités et des profils différents.
IV.1.	L’animateur (administrateur) 
	L’animateur est un acteur important dans le processus de veille. Il coordonne les activités de chaque acteur du système, assure la continuité et la cohérence du cycle d'information (ciblage, collecte, traitement, diffusion).	
	L'animation de la veille peut être confiée à une où à plusieurs personnes selon la politique de l’entreprise. A tire d’exemple, si le processus de stockage d’informations et connaissances est réparti auprès d'experts différents ; l’entreprise doit faire appel à plusieurs animateurs.
	En d’autres termes, l’animateur est un acteur dont son rôle est de consulté les informations et de diffuser celles qui sont déjà collectées et analysées et voit quelles est utiles pour les décideurs.

•	Profil de l’animateur	
	L'animateur doit avoir un double profil :
	Un profil de communication.
	Un profil de compétences techniques.

</p><p>IV.2.	Les veilleurs (capteurs, traqueurs, correspondants)
Ce sont les Personnes dans un environnement professionnel chargée d’assurer des fonctionnalités d’un système de veille.
	Ce sont eux qui se chargent de la recherche, la collecte, le traitement et l’envoi de l'information à l’animateur. Ils font leur recherche à partir 	du net, ils cherchent en permanence des informations sur des sujets variés. Il possède évidemment une bonne maîtrise et connaissance des outils de recherche d'information sur Internet (moteurs de recherche, agents intelligents, annuaires, etc.).
</p>IV.3.	Les décideurs(les employés)
	Les décideurs de l'entreprise sont les différente directions qui exploitées les résultats de veille diffusé par l’animateur. Ils exigent la remontée d'informations récentes, afin de prendre les décisions importante où opérationnelle pour l'entreprise.
